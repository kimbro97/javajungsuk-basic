public class Ex2_11 {
    public static void main(String[] args) {
        short sMin = -32768;
        short sMax = 32767;

        System.out.println("sMin = " + sMin);
        System.out.println("sMin-1 = " + (short) (sMin - 1));
        System.out.println("sMax = " + sMax);
        System.out.println("sMax+ = " + (short) (sMax + 1));
    }
}
